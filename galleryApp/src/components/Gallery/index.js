class Gallery extends Component {
  state = {activeImage: imagesList[0].id}

  onClickImage = value => {
    this.setState({activeImage: value})
  }

  changeImage = () => {
    const {activeImage} = this.state
    const getFilterImage = imagesList.filter(each => each.id === activeImage)
    return getFilterImage
  }

  render() {
    const {activeImage} = this.state
    const getFilterImage = this.changeImage()
    console.log(getFilterImage)
    return (
      <div className="bg-container">
        <li>
          {getFilterImage.map(eachfilterImage => (
            <img
              src={eachfilterImage.imageUrl}
              alt={eachfilterImage.imageAltText}
              className="image"
              key={eachfilterImage.id}
            />
          ))}
        </li>
        <h1>Nature Photography</h1>
        <p>Nature Photography by Rahul</p>
        <ul className="thumbnail-container">
          <li>
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                each={eachThumbnail}
                key={eachThumbnail.id}
                onClickImage={this.onClickImage}
                activeImage={activeImage === eachThumbnail.id}
              />
            ))}
          </li>
        </ul>
      </div>
    )
  }
}

export default Gallery
